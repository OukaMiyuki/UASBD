    public void show_data_to_table() throws SQLException{
        List<Kelas> keArr = controller.selectAll();
        txtPilihKelas.removeAllItems();
        tbModel.setRowCount(0);
        Object[] kolom = new Object[4];
        for (int i = 0; i < keArr.size(); i++) {
            kolom[0] = i+1;
            kolom[1] = keArr.get(i).getId_kelas();
            kolom[2] = keArr.get(i).getNama();
            kolom[3] = keArr.get(i).getKeterangan();
             
            tbModel.addRow(kolom);
            txtPilihKelas.addItem(keArr.get(i).getId_kelas());
         }
    }
    
    public void show_data_to_table_kereta() throws SQLException{
        List<Kereta> keArr = controler.selectAll();
        tbModelKereta.setRowCount(0);
        Object[] kolom = new Object[5];
        for (int i = 0; i < keArr.size(); i++) {
            kolom[0] = i+1;
            kolom[1] = keArr.get(i).getId_kereta();
            kolom[2] = keArr.get(i).getNama_Kereta();
            kolom[3] = keArr.get(i).getId_kelas();
            kolom[4] = keArr.get(i).getGerbong();
             
            tbModelKereta.addRow(kolom);
         }
    }
    
