    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        if(cekEmptyKereta()){
            JOptionPane.showMessageDialog(this, "One or more Fields are empty!");
        } else{
            kereta = new Kereta();
            kereta.setId_kereta(txtIdkereta.getText());
            kereta.setNama_Kereta(txtNama.getText());
            kereta.setId_kelas(txtPilihKelas.getSelectedItem().toString());
            kereta.setGerbong(Integer.parseInt(txtGerbong.getText()));
            try {
                controler.insertRecord(kereta);
                show_data_to_table_kereta();
                clearFields();
            } catch (SQLException ex) {
                Logger.getLogger(petugasCardMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

