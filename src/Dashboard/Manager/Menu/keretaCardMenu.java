    private void btnInsertKelasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertKelasActionPerformed
        if(cekEmpty()){
            JOptionPane.showMessageDialog(this, "One or more Fields are empty!");
        } else{
            kelas = new Kelas();
            kelas.setId_kelas(txtIdkelas.getText());
            kelas.setNama(txtNamakelas.getText());
            kelas.setKeterangan(txtKeterangankelas.getText());
            try {
                controller.insertRecord(kelas);
                show_data_to_table();
                clearFields();
            } catch (SQLException ex) {
                Logger.getLogger(petugasCardMenu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnInsertKelasActionPerformed

