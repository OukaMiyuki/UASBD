    private void btnPrevKelasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPrevKelasMouseClicked
        if(tbKelas.getSelectionModel().isSelectionEmpty()){
            tbKelas.setRowSelectionInterval(0, 0);
        } else{
            pos = tbKelas.getSelectedRow();
            pos--;
            if(pos < 0){
                pos = 0;
            }
            tbKelas.setRowSelectionInterval(pos, pos);
        }
        show_item_in_form(pos);
        tbKelas.setRowSelectionInterval(pos, pos);
        txtIdkelas.setEditable(false);
        btnUpdateKelas.setEnabled(true);
        btnDeleteKelas.setEnabled(true);
        btnInsertKelas.setEnabled(false);
    }//GEN-LAST:event_btnPrevKelasMouseClicked

    private void btnNextKelasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNextKelasMouseClicked
        if(tbKelas.getSelectionModel().isSelectionEmpty()){
            tbKelas.setRowSelectionInterval(0, 0);
        } else{
            pos = tbKelas.getSelectedRow();
            pos++;
            if(pos >= tbKelas.getRowCount()){
                pos = tbKelas.getRowCount()-1;
            }
            tbKelas.setRowSelectionInterval(pos, pos);
        }
        show_item_in_form(pos);
        tbKelas.setRowSelectionInterval(pos, pos);
        txtIdkelas.setEditable(false);
        btnUpdateKelas.setEnabled(true);
        btnDeleteKelas.setEnabled(true);
        btnInsertKelas.setEnabled(false);
    }//GEN-LAST:event_btnNextKelasMouseClicked

