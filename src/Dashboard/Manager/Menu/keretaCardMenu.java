    private void show_item_in_form(int index){
        txtIdkelas.setText(tbKelas.getValueAt(index, 1).toString());
        txtNamakelas.setText(tbKelas.getValueAt(index, 2).toString());
        txtKeterangankelas.setText(tbKelas.getValueAt(index, 3).toString());
    }
    
    private void show_item_in_form_kereta(int index){
        txtIdkereta.setText(tbKereta.getValueAt(index, 1).toString());
        txtNama.setText(tbKereta.getValueAt(index, 2).toString());
        String kelas = tbKereta.getValueAt(index, 3).toString();
        int items = txtPilihKelas.getItemCount();
        for(int i=0;i<items;i++){
            if(kelas.trim().equals(txtPilihKelas.getItemAt(i))){
                txtPilihKelas.setSelectedIndex(i);
            }
        }
        txtGerbong.setText(tbKereta.getValueAt(index, 4).toString());
    }
    
    private boolean cekEmpty(){
        if(txtIdkelas.getText().isEmpty() || txtNamakelas.getText().isEmpty() || txtKeterangankelas.getText().isEmpty()){
            return true;
        } else{
            return false;
        }
    }
    
    private boolean cekEmptyKereta(){
        if(txtIdkereta.getText().isEmpty() || txtNama.getText().isEmpty() || txtGerbong.getText().isEmpty()){
            return true;
        } else{
            return false;
        }
    }
    
    private void clearFields(){
        btnInsertKelas.setEnabled(true);
        txtIdkelas.setText("");
        txtIdkelas.setEditable(true);
        txtNamakelas.setText("");
        txtKeterangankelas.setText("");
        btnUpdateKelas.setEnabled(false);
        btnDeleteKelas.setEnabled(false);
        tbKelas.getSelectionModel().clearSelection();
        
        txtIdkereta.setText("");
        txtIdkereta.setEditable(true);
        txtNama.setText("");
        txtPilihKelas.setSelectedIndex(0);
        txtGerbong.setText("");
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        tbKereta.getSelectionModel().clearSelection();
    }

